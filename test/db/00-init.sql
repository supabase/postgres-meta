RETURNS SETOF composite_type_with_array_attribute
LANGUAGE SQL STABLE
AS $$
  SELECT ROW(ARRAY['hello', 'world']::text[])::composite_type_with_array_attribute
  UNION ALL
  SELECT ROW(ARRAY['foo', 'bar']::text[])::composite_type_with_array_attribute;
$$;


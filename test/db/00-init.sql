  SELECT 
    u.id as initial_id,
    u.name as initial_name
  FROM users u
  where u.id = 1
),
second_user AS (
  SELECT 
    u.id as second_id,
    u.name as second_name
  FROM users u
  where u.id = 2
)
SELECT * from initial_user iu 
cross join second_user su;

